#!/usr/bin/env node

const { execSync } = require('child_process');
const fs = require('fs');
const path = require('path');

/**
 * Comprehensive Test Runner for QuillHaven
 * 
 * This script runs all test suites and generates a comprehensive report
 */

const colors = {
  reset: '\x1b[0m',
  bright: '\x1b[1m',
  red: '\x1b[31m',
  green: '\x1b[32m',
  yellow: '\x1b[33m',
  blue: '\x1b[34m',
  magenta: '\x1b[35m',
  cyan: '\x1b[36m',
};

function log(message, color = colors.reset) {
  console.log(`${color}${message}${colors.reset}`);
}

function runCommand(command, description) {
  log(`\n${colors.cyan}Running: ${description}${colors.reset}`);
  log(`${colors.yellow}Command: ${command}${colors.reset}`);
  
  try {
    const startTime = Date.now();
    const output = execSync(command, { 
      stdio: 'pipe',
      encoding: 'utf8',
      maxBuffer: 1024 * 1024 * 10 // 10MB buffer
    });
    const endTime = Date.now();
    const duration = ((endTime - startTime) / 1000).toFixed(2);
    
    log(`${colors.green}✓ ${description} completed in ${duration}s${colors.reset}`);
    return { success: true, output, duration: parseFloat(duration) };
  } catch (error) {
    log(`${colors.red}✗ ${description} failed${colors.reset}`);
    log(`${colors.red}Error: ${error.message}${colors.reset}`);
    return { success: false, error: error.message, duration: 0 };
  }
}

function generateReport(results) {
  const reportPath = path.join(__dirname, '..', 'test-report.md');
  const timestamp = new Date().toISOString();
  
  let report = `# QuillHaven Test Report\n\n`;
  report += `**Generated:** ${timestamp}\n\n`;
  
  // Summary
  const totalTests = results.length;
  const passedTests = results.filter(r => r.success).length;
  const failedTests = totalTests - passedTests;
  const totalDuration = results.reduce((sum, r) => sum + r.duration, 0);
  
  report += `## Summary\n\n`;
  report += `- **Total Test Suites:** ${totalTests}\n`;
  report += `- **Passed:** ${passedTests}\n`;
  report += `- **Failed:** ${failedTests}\n`;
  report += `- **Success Rate:** ${((passedTests / totalTests) * 100).toFixed(1)}%\n`;
  report += `- **Total Duration:** ${totalDuration.toFixed(2)}s\n\n`;
  
  // Detailed Results
  report += `## Detailed Results\n\n`;
  
  results.forEach((result, index) => {
    const status = result.success ? '✅' : '❌';
    const duration = result.duration.toFixed(2);
    
    report += `### ${index + 1}. ${result.description}\n\n`;
    report += `- **Status:** ${status} ${result.success ? 'PASSED' : 'FAILED'}\n`;
    report += `- **Duration:** ${duration}s\n`;
    report += `- **Command:** \`${result.command}\`\n`;
    
    if (!result.success && result.error) {
      report += `- **Error:** \`${result.error}\`\n`;
    }
    
    report += `\n`;
  });
  
  // Performance Analysis
  report += `## Performance Analysis\n\n`;
  
  const performanceTests = results.filter(r => r.description.includes('Performance'));
  if (performanceTests.length > 0) {
    report += `### Performance Test Results\n\n`;
    performanceTests.forEach(test => {
      report += `- **${test.description}:** ${test.duration.toFixed(2)}s\n`;
    });
    report += `\n`;
  }
  
  // Coverage Analysis (if available)
  const coverageTest = results.find(r => r.description.includes('Coverage'));
  if (coverageTest && coverageTest.success) {
    report += `### Code Coverage\n\n`;
    report += `Coverage report generated. Check the coverage directory for detailed results.\n\n`;
  }
  
  // Recommendations
  report += `## Recommendations\n\n`;
  
  if (failedTests > 0) {
    report += `- **Fix Failed Tests:** ${failedTests} test suite(s) failed. Review the errors above and fix the issues.\n`;
  }
  
  const slowTests = results.filter(r => r.duration > 30);
  if (slowTests.length > 0) {
    report += `- **Optimize Slow Tests:** ${slowTests.length} test suite(s) took longer than 30 seconds. Consider optimization.\n`;
  }
  
  if (passedTests === totalTests) {
    report += `- **Great Job!** All tests are passing. Consider adding more edge case tests.\n`;
  }
  
  report += `\n---\n\n`;
  report += `*Report generated by QuillHaven Test Runner*\n`;
  
  fs.writeFileSync(reportPath, report);
  log(`\n${colors.green}Test report generated: ${reportPath}${colors.reset}`);
}

async function main() {
  log(`${colors.bright}${colors.blue}QuillHaven Comprehensive Test Suite${colors.reset}`);
  log(`${colors.blue}=====================================${colors.reset}`);
  
  const testSuites = [
    {
      command: 'npm run test:unit',
      description: 'Unit Tests',
    },
    {
      command: 'npm run test:integration',
      description: 'Integration Tests',
    },
    {
      command: 'npm run test:component',
      description: 'Component Tests',
    },
    {
      command: 'npm run test:e2e',
      description: 'End-to-End Tests',
    },
    {
      command: 'npm run test:performance',
      description: 'Performance Tests',
    },
    {
      command: 'npm run test:coverage',
      description: 'Coverage Report',
    },
    {
      command: 'npm run lint',
      description: 'Code Linting',
    },
    {
      command: 'npm run type-check',
      description: 'TypeScript Type Checking',
    },
  ];
  
  const results = [];
  
  for (const suite of testSuites) {
    const result = runCommand(suite.command, suite.description);
    results.push({
      ...result,
      command: suite.command,
      description: suite.description,
    });
    
    // Add a small delay between test suites
    await new Promise(resolve => setTimeout(resolve, 1000));
  }
  
  // Generate comprehensive report
  generateReport(results);
  
  // Final summary
  const passedCount = results.filter(r => r.success).length;
  const totalCount = results.length;
  
  log(`\n${colors.bright}${colors.blue}Test Execution Complete${colors.reset}`);
  log(`${colors.blue}======================${colors.reset}`);
  
  if (passedCount === totalCount) {
    log(`${colors.green}🎉 All ${totalCount} test suites passed!${colors.reset}`);
    process.exit(0);
  } else {
    log(`${colors.red}❌ ${totalCount - passedCount} out of ${totalCount} test suites failed${colors.reset}`);
    process.exit(1);
  }
}

// Handle process termination
process.on('SIGINT', () => {
  log(`\n${colors.yellow}Test execution interrupted${colors.reset}`);
  process.exit(1);
});

process.on('SIGTERM', () => {
  log(`\n${colors.yellow}Test execution terminated${colors.reset}`);
  process.exit(1);
});

// Run the main function
main().catch((error) => {
  log(`${colors.red}Fatal error: ${error.message}${colors.reset}`);
  process.exit(1);
});